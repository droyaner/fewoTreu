         <p>
	Die Stadt Pappenheim ist ein staatlich anerkannter Luftkurort (4000 Einwohner, 410 m.) und liegt abseits von 
	großen Verkehrsstrassen in einer Altmühlschleife zwischen Treuchtlingen und Eichstätt umgeben von herrlichen Mischwäldern. 
	Viele historische Bauwerke der ehemaligen Residenzstadt der Reichserbmarschälle zeugen von einer bedeutenden Vergangenheit. 
	Burg Pappenheim (1050), St.Galluskirche (9.Jhd.), Augustiner Kloster (1372), ev. Stadtkirche(1476), 
	Altes Schloss (1588) und Judenfriedhof (12. Jhd.).
